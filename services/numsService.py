class MathService:

    def plus_of_nums(self, num1: int, num2: int) -> int:
        return num1 + num2

    def min_of_nums(self, num1: int, num2: int) -> int:
        return num1 - num2

    def mult_of_nums(self, num1: int, num2: int) -> int:
        return num1 * num2

    def div_of_nums(self, num1: int, num2: int) -> float:
        return num1 / num2


mathService = MathService()
